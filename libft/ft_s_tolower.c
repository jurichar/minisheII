#include "libft.h"

char	*ft_s_tolower(char *s)
{
	size_t i;

	i = 0;
	while (s[i])
	{
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] = ft_tolower(s[i]);
		i++;
	}
	return (s);
}
